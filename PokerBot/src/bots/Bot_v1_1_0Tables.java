package bots;

/**
 * This enumeration is an enumeration of the positions of the tables.
 * This enumeration is needed for differentiation of the different positions of the tables.
 * 
 * @author Joschka J. Braun
 */
public enum Bot_v1_1_0Tables

{
	
	/**
	 * The table which is left-down on the screen.
	 */
	LEFT_DOWN,
	
	/**
	 * The table which is right-down on the screen.
	 */
	RIGHT_DOWN,
	
	/**
	 * The table which is left-up on the screen.
	 */
	LEFT_UP,
	
	/**
	 * The table which is right-up on the screen.
	 */
	RIGHT_UP
	
	
}
